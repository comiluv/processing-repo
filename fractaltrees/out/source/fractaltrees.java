public void branch(float len) {  
  line(0, 0, 0, -len);
  translate(0, -len);
  if (len>1) {
    pushMatrix();
    rotate(angle);
    branch(len * 0.67f);
    popMatrix();
    pushMatrix();
    rotate(-angle);
    branch(len * 0.67f);
    popMatrix();
  }
}

public void setAngle(float r) {
  angle = PI*r;
  float angle_degrees = angle / PI * 180;
  String textAngle = str(angle) +  "rads\n" + str(angle_degrees) +  "degrees"; 
  label2.setText(textAngle);
}
